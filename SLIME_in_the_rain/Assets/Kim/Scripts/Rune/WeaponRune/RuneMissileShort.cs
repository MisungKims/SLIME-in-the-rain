using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class RuneMissileShort : RuneWeapon
{
    #region 유니티 함수
    private void Awake()
    {
        weaponType1 = EWeaponType.dagger;
        weaponType2 = EWeaponType.sword;
    }
    #endregion

    #region 함수
    public override bool Use(Weapon weapon)
    {
        if (UseWeaponRune(weapon))          // 룬이 발동할 수 있는지 판별
        {
            Short shortWeapon = weapon.GetComponent<Short>();
            if (shortWeapon != null)
            {
                shortWeapon.IsHaveRune2 = true;       // 무기가 룬의 특성을 사용할 수 있도록
                return true;
            }
        }

        return false;
    }
    #endregion
}
